print("I will now count my chickens: ")
# These two will give us the number of Hens and Roosters
print("Hens", 25 + 30 / 6)
print("Roosters", float(100 - 25 * 3 % 4))
# Prompt for "egg count"
print("Now I will count the eggs: ")
# Calculation to find number of eggs
print(float(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6))
# Prompt the question
print("Is it true that 3 + 2 < 5 - 7?")
# Print whether it is True or False
# You don't use float() here because you want a True or False
print(3 + 2 < 5 - 7)
# These prompt the question AND answer, which is pretty cool
print("What is 3 + 2?", float(3 + 2))
print("What is 5 - 7?", float(5 - 7))
# Our reaction to the results
print("Oh, that's why it's False.")
# Prompt explaining we'll practice a few more
print("How about some more.")
# Again, cool combo of question + answer
# Again, lol, don't use float(), you want True or False output
print("Is it greater?", 5 > -2)
print("Is it greater or equal?", 5 >= -2)
print("Is it less or equal?", 5 <= -2)
